namespace AlcoholCalculator.Shared.Models;

public enum IntoxicationLevel
{
    Sober = 0,          // هوشیار
    Mild = 1,           // خفیف
    Euphoric = 2,       // سرخوش (لذت‌بخش)
    Impaired = 3,       // اختلال حسی-حرکتی
    Confused = 4,       // گیج و بی‌حال
    Stupor = 5,         // بی‌حسی (نزدیک به بیهوشی)
    Coma = 6,           // کما
    Lethal = 7          // مرگ‌آور
}
